package com.kn.package1;

public class Program1 {
	public int a=10;
	protected int b=20;
	int c=30;	//default or package modifier
	private int d=40;
	public void display1() {
		System.out.println("a = "+a);
		System.out.println("b = "+b);
		System.out.println("c = "+c);
		System.out.println("d = "+d);
	}

}
